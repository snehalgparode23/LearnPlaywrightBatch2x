let age = "Snehal"; //This is a string literal
let isStudent = true; //This is a boolean literal
let height = 5.6; //This is a number literal
let name = "John"; //This is a string literal
let nullValue = null; //This is a null literal
let undefinedValue; //This is an undefined literal

//typeof operator to check the type of literals
console.log(typeof age); // "string"
console.log(typeof isStudent);  // "boolean"
console.log(typeof height); // "number"
console.log(typeof name); // "string"
console.log(typeof nullValue); // "object" (this is a known quirk in JavaScript)
console.log(typeof undefinedValue); // "undefined"

