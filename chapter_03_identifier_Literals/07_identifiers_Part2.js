var name = "Snehal";

// Camel Case - starts with lowercase, each new word capitalized
// Commonly used for: variables, functions
var firstName = "Snehal";
var lastName = "Parode";


// Snake Case - lowercase words separated by underscores
// Commonly used for: constants (sometimes), database fields, file names
var first_name = "Amit";
var last_name = "Parode";
var user_age = 25;

// Pascal Case - starts with uppercase, each new word capitalized
// Commonly used for: class names, constructor functions, React components
var FirstName = "Snehal"; // less common for variables

// Screaming Snake Case (CONSTANT_CASE) - all uppercase with underscores
// Commonly used for: constants, environment variables, configuration values
var MAX_SIZE = 100;
var PI_VALUE = 3.14159;
var API_KEY = "abc123";
const DB_HOST = "localhost";

// Flat Case (all lowercase, no separators) - rarely used for multi-word
var firstname = "Snehal";
var username = "admin";

// Examples Summary
console.log("Camel Case:", firstName, lastName);
console.log("Snake Case:", first_name, last_name, user_age);
console.log("Pascal Case:", FirstName);
console.log("Screaming Snake Case:", MAX_SIZE, PI_VALUE, API_KEY);
console.log("Flat Case:", firstname, username);

